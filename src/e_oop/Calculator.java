package e_oop;

public class Calculator {

	float add(float num1, float num2){
		return num1 + num2;
	}
	float min(float num1, float num2){
		return num1 - num2;
	}
	float mul(float num1, float num2){
		return num1 * num2;
	}
	float div(float num1, float num2){
		return num1 / num2;
	}
	float rem(float num1, float num2){
		return num1 % num2;
	}
	
}
